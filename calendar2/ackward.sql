CREATE TABLE user(
	ID INT NOT NULL AUTO_INCREMENT,
	name VARCHAR(32) NOT NULL,
	password VARBINARY(255) NOT NULL,
	PRIMARY KEY (ID)
);

CREATE TABLE event(
	ID INT NOT NULL AUTO_INCREMENT,
	title VARCHAR(32) NOT NULL,
	start DATETIME NOT NULL,
	PRIMARY KEY (ID)
);

